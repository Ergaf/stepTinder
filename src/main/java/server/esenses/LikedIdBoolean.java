package server.esenses;

public class LikedIdBoolean {
    int id;
    boolean str;

    public LikedIdBoolean(int id, boolean str) {
        this.id = id;
        this.str = str;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public boolean getStr() {
        return str;
    }

    public void setStr(boolean str) {
        this.str = str;
    }
}
